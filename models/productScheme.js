import mongoose, { Schema } from "mongoose";

let productScheme = new Schema({
  name: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
});

let Product = mongoose.model("Product", productScheme);

export default Product;
